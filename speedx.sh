fechar_apps () {
clear
pi="bin.mt.plus com.termux me.piebridge.brevent"
ps=$(pm list packages -3)
IFS=$'\n'
for p in $ps; do
pn=$(echo "$p" | cut -d':' -f2)
case " $pi " in
*" $pn "*)
echo "Ignorados: $pn"
;;
*)
script
echo "Fechando app: $pn"
am force-stop "$pn"
;;
esac
done
sleep 0.5
ps=$(pm list packages | grep "^package:com.google")
il=("com.google.android.providers.media.module")
IFS=$'\n'
for p in $ps; do
pn=$(echo "$p" | cut -d':' -f2)
ignore=false
for ip in "${il[@]}"; do
if [[ "$pn" == *"$ip"* ]]; then
ignore=true
break
fi
done
if [ "$ignore" == true ]; then
echo "Ignorando pacote: $pn"
else
script
echo "Fechando app: $pn"
am force-stop "$pn"
fi
done
clear
echo "Todos aplicativos foram fechado com sucesso, voltando para o menu"
sleep 1.0
loading
sleep 2.0
menu_ram
}
